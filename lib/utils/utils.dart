        accuracy: 0,
        altitude: 0,
        altitudeAccuracy: 0,
        heading: 0,
        headingAccuracy: 0,
        speed: 0,
        speedAccuracy: 0,
      );
    } else {
      throw Exception('Failed to get location');
    }
  }


  static redirectMap({required String name, required double latitude, required double longLatitude}) async {
